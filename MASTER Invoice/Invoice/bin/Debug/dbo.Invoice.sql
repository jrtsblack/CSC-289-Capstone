CREATE TABLE [dbo].[Invoice] (
    [Invoice_ID]        INT IDENTITY (1, 1) NOT NULL,
    [Customer_ID]       INT NOT NULL,
    [OfficeWorker_ID]   INT NOT NULL,
    [ContractorCompany_ID] INT NULL,
    [Contractor_ID] INT NULL, 
    [Community_Name] NVARCHAR(30) NOT NULL, 
    [Occupant_Status] BIT NULL, 
    [Address] NVARCHAR(40) NOT NULL, 
    [Email] NVARCHAR(320) NOT NULL, 
    [Primary#] NVARCHAR(24) NOT NULL, 
    [Primary_Phone_Type] NVARCHAR(10) NULL, 
    [Primary_Extension] NVARCHAR(24) NULL, 
    [Alt#] NVARCHAR(24) NULL, 
    [Alt_Phone_Type] NVARCHAR(10) NULL, 
    [Alt_Extension] NVARCHAR(24) NULL, 
    [Permission_To_Enter] BIT NULL, 
    [TimeOfService] NVARCHAR(3) NULL, 
    [Due_Date] DATE NULL, 
    [Invoice_Type] NVARCHAR(15) NOT NULL, 
    [Description] NVARCHAR(MAX) NOT NULL, 
    [Complete] BIT NULL, 
    [Comments] NVARCHAR(MAX) NULL, 
    [Accepted] BIT NULL, 
    [Pets] BIT NOT NULL, 
    PRIMARY KEY CLUSTERED ([Invoice_ID] ASC),
    CONSTRAINT [FK_Invoice_InvoiceStatus] FOREIGN KEY ([ContractorCompany_ID]) REFERENCES [dbo].[InvoiceStatus] ([Invoice_Status_ID]),
    CONSTRAINT [FK_Invoice_Customer] FOREIGN KEY ([Customer_ID]) REFERENCES [dbo].[Customer] ([Customer_ID]),
    CONSTRAINT [FK_Invoice_OfficeWorker] FOREIGN KEY ([OfficeWorker_ID]) REFERENCES [dbo].[OfficeWorker] ([OfficeWorker_ID])
);

